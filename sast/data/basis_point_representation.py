import numpy as np
from scipy.spatial import KDTree


class RelativeBasisPointSet:
    """
    Calculates the BPS from one 3D object to another
    """

    def __init__(self) -> None:
        pass

    def query(self, src: np.ndarray, tgt: np.ndarray) -> np.ndarray:
        """
        :param src: {n_src x 3}
        :param tgt: {n_tgt x 3}
        returns:
            {n_src}
        """
        tree = KDTree(tgt)
        d, _ = tree.query(src)
        return np.array(d, dtype=np.float32)


